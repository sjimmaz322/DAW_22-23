    private void btnAniadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAniadirActionPerformed
        // TODO add your handling code here:
        String rolPreferido = radioDM.isSelected() ? "DM" : "PJ";
        Jugadores j = new Jugadores();
        j.setApodo(campoApodo.getText());
        j.setSistemaPredilecto(campoSistema.getText());
        j.setRolPreferido(rolPreferido);
        String nombre = listaUsuario.getSelectedValue();
        Usuarios user = ujc.findUsuariosEntities()
                .stream()
                .filter(u -> u.getNombre().equalsIgnoreCase(nombre))
                .findFirst()
                .get();

        user.setCodUsuario(j.getId());

        jjc.create(j);

        j.setCodUsuario(user);
        user.setJugador(j);
    }//GEN-LAST:event_btnAniadirActionPerformed

