    private void cargarValores() {
        Jugadores u = jjc.findJugadores(valor);
        //JOptionPane.showMessageDialog(null, u);
        campoApodo.setText(u.getApodo());
        campoSistema.setText(u.getSistemaPredilecto());
        if (u.getRolPreferido().equalsIgnoreCase("DM")) {
            rDM.setSelected(true);
        } else {
            rPJ.setSelected(true);
        }
    }

