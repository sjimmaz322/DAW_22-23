    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        Jugadores u = jjc.findJugadores(valor);
        u.setApodo(campoApodo.getText());
        u.setSistemaPredilecto(campoSistema.getText());
        if (rDM.isSelected()) {
            u.setRolPreferido("DM");
        } else if (rPJ.isSelected()) {
            u.setRolPreferido("PJ");
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione una de las opciones proporcionadas");
        }

        try {
            jjc.edit(u);
            JOptionPane.showMessageDialog(null, "Jugador modificado con éxito");
            this.dispose();
        } catch (Exception ex) {
            Logger.getLogger(EdicionJugador.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnModificarActionPerformed

